#include "do_op.h"

int	myadd(int a, int b)
{
	return (a + b);
}

int	mymin(int a, int b)
{
	return (a - b);
}

int	mymul(int a, int b)
{
	return (a * b);
}

int	mydiv(int a, int b)
{
	return (a / b);
}

int	mymod(int a, int b)
{
	return (a % b);
}
