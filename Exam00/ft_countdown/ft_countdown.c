#include <unistd.h>

int		ft_countdown(void)
{
	int		n;
	char	c;

	n = '9';
	c = '\n';
	while (n >= '0')
	{
		write(1, &n, 1);
		n--;
	}
	write(1, &c, 1);
	return (0);
}

int		main(void)
{
	ft_countdown();
	return (0);
}
