char	*ft_strcapitalize(char *str)
{
	int i;
	int j;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[0] >= 'a' && str[0] <= 'z')
			str[0] -= 32;
		i++;
		if ((str[i] >= 'a' && str[i] <= 'z') &&
		(str[i - 1] == 32 || str[i - 1] == 43 || str[i - 1] == 45))
			str[i] -= 32;
		else if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] += 32;
	}
	return (str);
}
