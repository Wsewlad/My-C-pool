#include "rush_02.h"

int	*ft_strlen_n(char *str)
{
	int *n;

	n = (int*)malloc(sizeof(int) * 7);
	n[0] = 0;
	n[1] = 0;
	n[2] = 0;
	n[3] = 0;
	n[4] = 0;
	n[5] = 0;
	n[6] = 0;
	while (str[n[5]] != '\0')
	{
		if (str[n[5]] == '\n')
			n[6]++;
		n[5]++;
	}
	return (n);
}
