int		ft_atoi(char *str)
{
	int i;
	int numb;

	i = 0;
	numb = 0;
	while (str[i] >= '0' && str[i] <= '9')
	{
		numb = numb * 10 + 'str[i]' - '0';
		i++;
	}
	return (numb);
}
