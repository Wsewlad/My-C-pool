#include "eval_expr.h"

void	ft_putnbr(int nb)
{
	long int	i;
	long int	nb2;

	i = 1;
	nb2 = nb;
	if (nb2 < 0)
	{
		ft_putchar(45);
		nb2 *= -1;
	}
	while (nb2 / i > 9)
	{
		i *= 10;
	}
	while (i != 0)
	{
		ft_putchar(nb2 / i + 48);
		nb2 %= i;
		i /= 10;
	}
}
