#include <unistd.h>

int		d_m_z(char o, int val2)
{
	if (val2 == 0)
	{
		if (o == '/')
		{
			write(1, "Stop : division by zero\n", 24);
			return (0);
		}
		if (o == '%')
		{
			write(1, "Stop : modulo by zero\n", 22);
			return (0);
		}
	}
	return (1);
}
