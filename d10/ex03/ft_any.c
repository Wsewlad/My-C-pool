int		ft_any(char **tab, int (*f)(char*))
{
	int		i;
	int		n;

	i = 0;
	while (tab[i] != 0)
	{
		n = f(tab[i]);
		if (n == 1)
			return (1);
		i++;
	}
	return (0);
}
