char	*ft_rot42(char *str)
{
	int i;
	int n;

	n = 42;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 65 && str[i] <= 90)
			str[i] = (str[i] - 65 + n) % 26 + 65;
		if (str[i] >= 97 && str[i] <= 122)
			str[i] = (str[i] - 97 + n) % 26 + 65;
		i++;
	}
	return (str);
}
